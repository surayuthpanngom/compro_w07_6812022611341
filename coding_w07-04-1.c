#include <stdio.h>

int main() {
    int level;

    // รับข้อมูลระดับสมาชิกจากผู้ใช้
    printf("กรุณาป้อนระดับสมาชิกของคุณ (1=Silver, 2=Gold, 3=Platinum, 4=Diamond): ");
    scanf("%d", &level);

    printf("สิทธิประโยชน์ของคุณคือ: ");

    // ใช้ switch-case ในการตรวจสอบเงื่อนไข
    switch (level) {
        case 1:
            printf("5%% discount\n");
            break; // หยุดการทำงานตอนจบ case
        case 2:
            printf("10%% discount + Reward points\n");
            break;
        case 3:
            printf("15%% discount + Reward points + Birthday gift\n");
            break;
        case 4:
            printf("ได้ทุกอย่าง + VIP events\n");
            break;
        default:
            // กรณีที่ค่าไม่ตรงกับ case ไหนเลย
            printf("ไม่พบระดับสมาชิกที่ระบุ\n");
            break;
    }

    return 0;
}

//ความแตกต่างระหว่าง if-else if-else และ switch-case
//if-else if-else เป็นโครงสร้างการตัดสินใจที่ยืดหยุ่นมากที่สุด เหมาะสำหรับการเปรียบเทียบเงื่อนไขที่ซับซ้อน หรือการเปรียบเทียบกับค่าที่ไม่ใช่ตัวเลข
//ความยืดหยุ่น: สามารถใช้กับเงื่อนไขที่หลากหลาย เช่น > (มากกว่า), < (น้อยกว่า), && (และ), || (หรือ)
//การเปรียบเทียบ: ใช้ได้กับการเปรียบเทียบค่าที่ไม่เท่ากัน เช่น if (score >= 80) หรือ if (age < 18 && has_permission == true)
//การใช้งาน:เหมาะสำหรับเงื่อนไขที่ต้องตรวจสอบช่วงของค่า (range) หรือการเปรียบเทียบที่ซับซ้อน

//switch-case เป็นโครงสร้างที่ออกแบบมาเพื่อจัดการกับกรณีที่มีการเปรียบเทียบค่าคงที่หลาย ๆ ค่าจากตัวแปรตัวเดียว
//ความแม่นยำ: ทำงานได้เร็วและมีประสิทธิภาพสูงกว่า if-else if-else ในกรณีที่ต้องเปรียบเทียบกับค่าคงที่จำนวนมาก
//การเปรียบเทียบ: ใช้ได้เฉพาะการเปรียบเทียบความเท่ากัน (==) กับค่าคงที่ที่เป็นจำนวนเต็มหรือตัวอักษรเท่านั้น
//การใช้งาน: เหมาะสำหรับเมนูหรือตัวเลือกที่มีจำนวนจำกัด เช่น การเลือกวันในสัปดาห์ (1-7) หรือการเลือกระดับสมาชิก (1-4)
//คำสั่ง break: จำเป็นต้องใช้คำสั่ง break เพื่อป้องกันไม่ให้โปรแกรมทำงาน ไปยัง case ถัดไป